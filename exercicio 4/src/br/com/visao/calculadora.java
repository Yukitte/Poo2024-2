    private void jSomarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSomarActionPerformed
        if(jTvalor1.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Campo Obrigat贸rio");
            jTvalor1.requestFocus();
        }else if(jTvalor2.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Campo Obrigat贸rio");
            jTvalor2.requestFocus();
        }
        else{
             try{
                c.setN1(Double.valueOf( jTvalor1.getText()));
                c.setN2(Double.valueOf( jTvalor2.getText()));
                jLresultado.setText(""+c.somar());
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Apenas Numeros");
                    jTvalor1.requestFocus();
            }
        }
    }//GEN-LAST:event_jSomarActionPerformed

